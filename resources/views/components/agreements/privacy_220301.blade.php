- 필수항목 : DISCORD 사용자명, NAVER 아이디, STEAM 고유 번호, STEAM 닉네임, 생년월일

### 서비스 제공
- 필수항목 : DISCORD 사용자명, NAVER 아이디, STEAM 고유 번호, STEAM 닉네임, 생년월일, 사용자 IP 주소, 사용자 컴퓨터 이름

### 서비스 개선
- 필수항목 : STEAM 고유 번호

### 고충처리
- 필수항목 : DISCORD 사용자명, NAVER 아이디, STEAM 고유 번호, STEAM 닉네임, 생년월일

## 제6조(개인정보의 파기)
MGM은 개인정보 보유기간의 경과, 처리목적 달성 등 개인정보가 불필요하게 되었을 때에는 지체없이 해당 개인정보를 파기합니다.
정보주체로부터 동의받은 개인정보 보유기간이 경과하거나 처리목적이 달성되었음에도 불구하고 다른 법령에 따라 개인정보를 계속 보존하여야 하는 경우에는, 해당 개인정보를 별도의 데이터베이스(DB)로 옮기거나 보관장소를 달리하여 보존합니다.
개인정보 파기의 절차 및 방법은 다음과 같습니다.

### 파기절차
MGM은 파기 사유가 발생한 개인정보를 선정하고, 갤럭시허브의 개인정보 보호책임자의 승인을 받아 개인정보를 파기합니다.

### 파기방법
MGM은 전자적 파일 형태로 기록․저장된 개인정보는 기록을 재생할 수 없도록 파기합니다.

