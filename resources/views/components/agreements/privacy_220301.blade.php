멀티플레이게임매니지먼트(이하 MGM)는 개인 정보 보호법 제30조에 따라 정보주체의 개인정보를 보호하고 이와 관련된 고충을 신속하고 원활하게 처리할 수 있도록 하기 위하여 다음과 같이 개인정보 처리방침을 수립․공개합니다.

## 제1조(개인정보의 처리목적)
MGM은 다음의 목적을 위하여 개인정보를 처리합니다. 처리하고 있는 개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며, 이용 목적이 변경되는 경우에는 개인정보 보호법 제18조에 따라 별도의 동의를 받는 등 필요한 조치를 이행할 예정입니다.

### 회원 가입 및 관리
회원 가입의사 확인, 회원제 서비스 제공에 따른 본인 식별․인증, 회원자격 유지․관리, 제한적 본인확인제 시행에 따른 본인확인, 서비스 부정이용 방지, 만 14세 미만 아동의 개인정보 처리시 법정대리인의 동의여부 확인, 각종 고지․통지, 고충처리 등을 목적으로 개인정보를 처리합니다.

### 서비스 제공
서비스 제공, 콘텐츠 제공, 본인인증, 연령확인 등을 목적으로 개인정보를 처리합니다.

### 서비스 개선
서비스 개선을 위한 목적으로 개인정보를 단방향 암호화하여 처리합니다.

### 고충처리
신청 회원의 신원 확인, 민원사항 확인, 사실조사를 위한 연락․통지, 처리결과 통보 등의 목적으로 개인정보를 처리합니다.

## 제2조(개인정보의 처리 및 보유기간)
MGM은 법령에 따른 개인정보 보유․이용기간 또는 정보주체로부터 개인정보를 수집 시에 동의 받은 개인정보 보유․이용기간 내에서 개인정보를 처리․보유합니다.
각각의 개인정보 처리 및 보유 기간은 다음과 같습니다.

### 회원 가입 및 관리
MGM이 제공하는 서비스 탈퇴 시까지
다만, 다음의 사유에 해당하는 경우에는 해당 기간 종료 시까지
- 가입거절자의 중복가입확인정보: 회원 탈퇴일로부터 5년

### 서비스 제공
MGM이 제공하는 서비스 탈퇴 시까지

### 서비스 개선
회원 탈퇴일로부터 5년까지

### 고충처리
MGM이 제공하는 서비스 탈퇴 시까지

## 제3조(개인정보의 제3자 제공)
MGM은 정보주체의 개인정보를 제1조(개인정보의 처리 목적)에서 명시한 범위 내에서만 처리하며, 개인정보를 제3자에게 제공하고 있지 않습니다.

## 제4조(개인정보처리의 위탁)
MGM은 개인정보 처리업무를 위탁하지 않습니다.

## 제5조(처리하는 개인정보 항목)
MGM은 다음의 개인정보 항목을 처리하고 있습니다.

